package Practice_Programs;

public class sumOfN_NaturalNumber {
    public static void main(String[] args) {
        int n=30;
        int i=0;
        int sum=0;
        while(i<=n){
            sum=sum+i;
            System.out.println(i+"  >>-->  "+sum);
            i++;
        }
        System.out.println(12>>1);
    }
}
